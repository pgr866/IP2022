package org.ip.sesion03;

import java.util.Scanner;

public class Fibonacci {

	public static void main(String[] args) {
	
		int f1 = 0;
		int f2 = 1;
		int f3 = 1;
		int i = 1;
		int n;
		
		System.out.println("Hasta que termino de la serie Fibonacci quieres mostrar?");
		Scanner entrada=new Scanner(System.in);
		n = entrada.nextInt();
		entrada.close();
		
		System.out.print("0  1  ");
		while (i<n) {
			f3 = f1 + f2;
			System.out.print(f3 + "  ");
			f1 = f2;
			f2 = f3;
			i++;
			
		}	
	}
}
