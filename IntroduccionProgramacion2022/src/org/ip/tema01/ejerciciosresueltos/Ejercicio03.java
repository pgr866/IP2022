package org.ip.tema01.ejerciciosresueltos;

public class Ejercicio03 {

	/**
	 * A partir del codigo de un articulo, muestreapor pantalla un mensaje que
	 * indica si el codigo es o no defectusoso. Codigos defectuosos: 14681 hasta
	 * 15681 ambos inclusive 70001 hasta 79999 ambos inclusive 99999 hasta
	 * 110110 ambos inclusive
	 * 
	 * @param args
	 */
	public static void main(String[] args) {
		int codigo;
		codigo = 50000; // Ejecutar cambiando el valor a 3000000000
		if ((codigo >= 14681) && (codigo <= 15681) || (codigo >= 70001)
				&& (codigo <= 79999) || (codigo >= 99999) && (codigo <= 110110)) {
			System.out.println("CODIGO DEFECTUOSO");
		} else {
			System.out.println("CODIGO CORRECTO");
		}
	}

}
