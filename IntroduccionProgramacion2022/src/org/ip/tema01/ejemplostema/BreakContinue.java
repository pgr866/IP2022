package org.ip.tema01.ejemplostema;

public class BreakContinue {

	public static void main(String[] args) {
		int i;
		i = 1;
		while (i <= 10) {
			System.out.println("La i vale " + i);
			if (i == 2) {
				break;
			}
			i++;
		}
		
		System.out.println();
		
		i = 0;
		while (i < 10) {
			i++;
			if (i % 2 == 0) {
				continue;
			}
			System.out.println("La i vale " + i);
		}
	}
}
