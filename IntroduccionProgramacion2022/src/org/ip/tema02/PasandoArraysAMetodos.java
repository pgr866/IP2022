package org.ip.tema02;

public class PasandoArraysAMetodos {

	public static void main(String[] args) {
		int x = 1;
		int[] y = new int[10];
		y[0] = 1;
		System.out.println("x = " + x);
		System.out.println("y[0] = " + y[0]);
		metodo(x, y);
		System.out.println("x = " + x);
		System.out.println("y[0] = " + y[0]);
	}
	
	public static void metodo(int numero, int[] array) {
		numero = 1001;
		array[0] = 7777;
	}
}
